package hr.fer.zemris.java.tecaj_1;

/**
 * Primjer klase koja definira tri svojstva:
 * oznaku, brojac i placu.
 * 
 * @author lala
 * @version 1.0
 */
public class Primjer {
    private String oznaka;
    private int brojac;
    private double placa;

    public String getOznaka() {
        return oznaka;
    }

    public void setOznaka(String oznaka) {
        this.oznaka = oznaka;
    }

    public void setBrojac(int brojac) {
        this.brojac = brojac;
    }

    public double getPlaca() {
        return placa;
    }

}